# NUR TOURNAMENTS — инструкция по запуску

Это уже настоящий сайт: регистрация, команды и турниры хранятся в общей базе
данных (Supabase), а не только у тебя в браузере. Ниже — пошагово, что нужно
сделать, чтобы он заработал у тебя на домене.

## Обновление дизайна (логотип, промо-баннер, красная тема)

В папке `public/` лежат два файла — `logo.jpg` (твой логотип из аватарки) и
`promo.jpg` (баннер турнира NUR FAST CUP 2v2). Сайт сам их подхватывает и
показывает: логотип — в шапке сайта, баннер — кликабельная плашка под шапкой,
которая ведёт в Telegram-канал `https://t.me/tourNUR`.

**Если ссылка на канал неверная** — открой `src/App.jsx`, найди строку
```
<a href="https://t.me/tourNUR" ...
```
и замени адрес на настоящий.

**Если захочешь заменить сами картинки** — просто загрузи новые файлы с теми
же именами (`logo.jpg`, `promo.jpg`) в папку `public` в GitHub, перезаписав
старые — Vercel передеплоит сайт автоматически.

## Шаг 1. Supabase (база данных + регистрация)

1. Зайди на https://supabase.com и зарегистрируйся (можно через GitHub).
2. Нажми **New project**. Придумай пароль от базы (сохрани его куда-нибудь).
3. Когда проект создастся — слева открой **SQL Editor** → **New query**.
4. Скопируй туда весь файл `supabase-schema.sql` из этого архива и нажми **Run**.
   Это создаст все таблицы и настройки безопасности.
5. Слева открой **Authentication → Providers** и убедись, что **Email**
   включён. Для удобства тестирования можно на вкладке **Authentication →
   Settings** отключить "Confirm email" — тогда после регистрации не нужно
   будет подтверждать почту (для боевого сайта потом можно включить обратно).
6. Открой **Project Settings → API** — там будет:
   - **Project URL** → это `VITE_SUPABASE_URL`
   - **anon public key** → это `VITE_SUPABASE_ANON_KEY`
   Сохрани их — понадобятся в Шаге 3.

## Шаг 2. GitHub (просто чтобы Vercel забрал код)

1. Зайди на https://github.com и зарегистрируйся (или войди), если ещё не был.
2. Нажми **New repository**, назови как хочешь (например `nur-tournaments`),
   оставь **Public** или **Private** — без разницы, жми **Create repository**.
3. На странице нового репозитория будет кнопка **uploading an existing file** —
   перетащи туда ВСЕ файлы и папки из этого архива (кроме `.env.example`,
   его загружать не обязательно) и нажми **Commit changes**.

## Шаг 3. Vercel (хостинг + подключение домена)

1. Зайди на https://vercel.com и зарегистрируйся через GitHub (кнопка
   "Continue with GitHub") — так аккаунты сразу свяжутся.
2. Нажми **Add New → Project**, выбери репозиторий, который ты только что
   создал.
3. Vercel сам определит, что это Vite-проект. Перед деплоем разверни
   **Environment Variables** и добавь две переменные:
   - `VITE_SUPABASE_URL` = твой Project URL из Supabase
   - `VITE_SUPABASE_ANON_KEY` = твой anon public key из Supabase
4. Нажми **Deploy**. Через минуту сайт будет доступен по адресу вида
   `nur-tournaments.vercel.app`.

## Шаг 4. Подключение своего домена

1. Купи домен на любом регистраторе (Reg.ru, Namecheap и т.п.).
2. В Vercel открой свой проект → **Settings → Domains** → введи купленный
   домен → **Add**.
3. Vercel покажет DNS-записи (обычно A-запись или CNAME), которые нужно
   добавить в панели управления твоего регистратора домена.
4. После добавления записей подожди от нескольких минут до пары часов —
   домен привяжется автоматически, Vercel сам выпустит SSL-сертификат
   (https).

## Шаг 5. Стать админом

Чтобы получить доступ к вкладке «Админ» на сайте:

1. Зарегистрируйся на самом сайте как обычный пользователь.
2. В Supabase открой **Table Editor → profiles**.
3. Найди свою строку (по username) и поставь галочку в колонке `is_admin`.
4. Обнови страницу сайта — вкладка «Админ» станет доступна.

## Локальный запуск (по желанию, для проверки перед загрузкой)

Если у тебя установлен Node.js:

```
npm install
cp .env.example .env
# впиши свои VITE_SUPABASE_URL и VITE_SUPABASE_ANON_KEY в .env
npm run dev
```

Откроется адрес вида `http://localhost:5173`.

## Если что-то не работает

- Белый экран / ошибка про Supabase — проверь, что переменные окружения
  указаны верно (и в Vercel, и локально в `.env`).
- Не получается зарегистрироваться — проверь в Supabase, включён ли Email
  provider и не требует ли он подтверждения почты (Шаг 1, пункт 5).
- Нет вкладки «Админ» — см. Шаг 5.
